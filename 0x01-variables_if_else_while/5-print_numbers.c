#include <stdlib.h>
#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: 'This program outputs all base 10 numbers
 * high or lower than 5'
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	int count;

	for (count = 0; count < 10; count++)
	{
		printf("%d\n", count);
	}

	return (0);
}
