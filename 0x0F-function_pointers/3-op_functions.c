#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: num 1
 * @b: num 2
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_add - subtracts 2 integers
 * @a: num 1
 * @b: num 2
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_add - multiplies 2 integers
 * @a: num 1
 * @b: num 2
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_add - divides 2 integers
 * @a: num 1
 * @b: num 2
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_add - modulo of 2 integers
 * @a: num 1
 * @b: num 2
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
